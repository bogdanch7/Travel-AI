/**
 * Resolves an IATA code (airport or city) to the preferred Vola CITY code.
 * Example: OTP -> BUH, LHR -> LON
 */
export function resolveVolaCityCode(code: string): string {
  const upper = code.toUpperCase().trim();
  return AIRPORT_TO_CITY[upper] || upper;
}

/**
 * Resolves a city name to an IATA code using an exhaustive mapping.
 */
export function resolveCityNameToCode(name: string): string | null {
  const normalized = name.toUpperCase().trim();
  // Remove trailing colon often found in city lists
  const clean = normalized.replace(/:$/, '');
  return CITY_NAME_TO_CODE[clean] || null;
}
